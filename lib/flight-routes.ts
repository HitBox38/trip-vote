/**
 * Get all countries reachable from an origin country (including the origin itself)
 */
export function getReachableCountries(originCode: string): string[] {
  const routes = FLIGHT_ROUTES[originCode] || [];
  // Include the origin country in the reachable list
  return [originCode, ...routes];
}

/**
 * Check if a country is reachable from an origin country
 */
export function isCountryReachable(originCode: string, destinationCode: string): boolean {
  if (originCode === destinationCode) return true;
  const routes = FLIGHT_ROUTES[originCode] || [];
  return routes.includes(destinationCode);
}
